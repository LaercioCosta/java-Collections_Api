package main.java.list.exercios.pesquisa;

public class soma {
    private int numeros;

    public soma(int numeros) {
        this.numeros = numeros;
    }

    public int getNumeros() {
        return numeros;
    }

    @Override
    public String toString() {
        return "soma{" +
                "numeros=" + numeros +
                '}';
    }
}
